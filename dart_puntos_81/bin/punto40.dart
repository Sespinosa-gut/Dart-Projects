double anterior = 120.0;
double actual = 350.0;

void main() {
  double consumo = actual - anterior;
  double costo = 0;

  if (consumo >= 0 && consumo <= 100) {
    costo = 2.622 * consumo;
  } else if (consumo >= 101 && consumo <= 300) {
    costo = 79.78 * consumo;
  } else if (consumo >= 301 && consumo <= 500) {
    costo = 89.52 * consumo;
  } else if (consumo >= 501) {
    costo = 97.95 * consumo;
  } else {
    return;
  }

  print("Consumo total: $consumo Kwh");
  print("Monto a pagar: $costo");
}

